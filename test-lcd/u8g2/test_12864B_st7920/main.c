int main(void)
{
    uint32_t screen = 0;
    u8g2_t u8g2;

    /* initialize to SPI */
    puts("Initializing to SPI.");

    u8g2_Setup_st7920_s_128x64_1(&u8g2, U8G2_R0, u8x8_byte_hw_spi_riotos, u8x8_gpio_and_delay_riotos);

    u8x8_riotos_t user_data =
    {
        .device_index = SPI_DEV(0),
        .pin_cs =  GPIO_PIN(PORT_A, 4),
        .pin_dc =  GPIO_UNDEF,//GPIO_UNDEF,
        .pin_reset =  GPIO_PIN(PORT_A, 0),
    };

    u8g2_SetUserPtr(&u8g2, &user_data);

    /* initialize the display */
    puts("Initializing display.");

    u8g2_InitDisplay(&u8g2);
    u8g2_SetPowerSave(&u8g2, 0);

    /* start drawing in a loop */
    puts("Drawing on screen.");

    while (1) {
        u8g2_FirstPage(&u8g2);

        do {
            u8g2_SetDrawColor(&u8g2, 1);
            u8g2_SetFont(&u8g2, u8g2_font_helvB12_tf);

            switch (screen) {
                case 0:
                    u8g2_DrawStr(&u8g2, 12, 22, "THIS");
                    break;
                case 1:
                    u8g2_DrawStr(&u8g2, 24, 22, "IS");
                    break;
                case 2:
                    u8g2_DrawBitmap(&u8g2, 0, 0, 8, 32, logo);
                    break;
            }
        } while (u8g2_NextPage(&u8g2));

        /* show screen in next iteration */
        screen = (screen + 1) % 3;

        /* sleep a little */
        xtimer_sleep(1);
    }

    return 0;
}
